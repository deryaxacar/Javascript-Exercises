var number = 5;

number++; 
document.write( "number++ : " + number + "<br>"); // 6

number--;
document.write("number-- : " + number + "<br>"); // 5

number += 1;
document.write("number += 1 : " + number + "<br>"); // 6

number -= 1;
document.write("number -= 1 : " + number + "<br>"); // 5

number = number + 1;
document.write("number + 1 : " + number + "<br>"); // 6

number = number - 1;
document.write("number - 1 : " + number + "<br>"); // 5
