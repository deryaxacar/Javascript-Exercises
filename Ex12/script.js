var number = 10; // tek eşittir atama işlemi yapar.

if(number == 10) {
    // iki == eşittir karşılaştırma işlemi yapar.
    document.write("number is equal to 10");
}

number += 5; // += toplama işlemi yapar.
document.write("<br>");
document.write("number: " + number); // number: 15

number -= 5; // -= çıkarma işlemi yapar.
document.write("<br>");
document.write("number: " + number); // number: 10

number *= 5; // *= çarpma işlemi yapar.
document.write("<br>");
document.write("number: " + number); // number: 50

number /= 5; // /= bölme işlemi yap
document.write("<br>");
document.write("number: " + number); // number: 10

number %= 5; // %= mod alma işlemi yapar.
document.write("<br>");
document.write("number: " + number); // number: 0

